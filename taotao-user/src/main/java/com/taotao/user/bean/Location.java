package com.taotao.user.bean;

public class Location {
     private String x;
     private String y;
     private String h;
     private String w;
	public String getX() {
		return x;
	}
	public void setX(String x) {
		this.x = x;
	}
	public String getY() {
		return y;
	}
	public void setY(String y) {
		this.y = y;
	}
	public String getH() {
		return h;
	}
	public void setH(String h) {
		this.h = h;
	}
	public String getW() {
		return w;
	}
	public void setW(String w) {
		this.w = w;
	}
     
     
     
}
